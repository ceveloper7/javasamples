/**
 * Data types in Java. En java existen 8 tipos de datos primitivos
 * Integer primitive type
 *  byte -> 1 byte -> -128 / 127
 *  short -> 2 byte -> -32,768 / 32,767
 *  int -> 4 byte -> -2,147,483,648 / 2,147,483,647
 *  long -> 8 byte -> -9,223,372,036,854,775,808 / 9,223,372,854,775,807
 *
 * Float point primitive type
 *  float -> 4 byte -> Approximately ±3.40282347 ×1038 (6–7 significant decimal digits)
 *  double -> 8 byte -> Approximately ±1.79769313486231570 ×10308 (15 significant decimal digits)
 *
 * Character primitive type
 *  char
 *
 * Boolean primitive type
 *  boolean -> true / false
 *
 * Sufijo y prefijo en Numbers
 * Hexadecimal number -> 0x / 0X
 * Octal number -> 0
 * Binary number -> 0b / 0B
 * Long number -> 4000000000L
 *
 */

